// server/prompts/extracting.js

export function getExtractingPrompt(transcript) {
  return `あなたは会議用語の抽出専門家です。
会議の文字起こしから、**実際に説明・定義された用語**を抽出してください。

【重要な方針】
1. **質を重視**: 10-15個の有用な用語を抽出（量より質）
2. **会議で実際に説明された用語を優先**: 「〜とは」「〜っていうのは」など
3. **専門用語・固有名詞を重視**: 一般的な言葉より、その会議特有の用語
4. **文脈は具体的に**: 「どういう場面で使われたか」を明確に

【出力するJSON構造】
{
  "terms": [
    {
      "term": "用語名",
      "isAmbiguous": false,
      "contexts": [
        {
          "context": "どういう場面・話題で使われたか（1-2文で具体的に）",
          "definition": "この用語の意味・指すもの（具体的に）",
          "examples": [
            "実際の発言例1",
            "実際の発言例2"
          ],
          "frequency": 5
        }
      ]
    }
  ]
}

【抽出すべき用語の優先順位】

**最優先（必ず抽出）:**
1. **明示的に説明された用語**
   - 「〜とは〜です」「〜っていうのは〜」で説明されたもの
   - 例: 「AI-BPOというのは、AIを使った業務プロセスの外部委託です」
   
2. **専門用語・業界用語**
   - その会議・業界特有の言葉
   - 例: 就労継続支援、ニーズカード、STT
   
3. **固有名詞（会議に関連するもの）**
   - 製品名: Gemini, Deepgram, BigQuery
   - 会社名: Thankslab
   - プロジェクト名: meeting-tool-v2
   - サービス名: Looker, n8n

**中優先（関連があれば抽出）:**
4. **プロジェクト固有の用語**
   - チーム内でしか通じない表現
   - 内部で使っている略語
   
5. **技術用語（会議で説明されたもの）**
   - 例: WebSocket, クリーニング処理

**低優先（説明がある場合のみ）:**
6. **抽象的な評価語（具体的に説明された場合）**
   - 「スムーズに」→「どういう状態か」が説明されている場合のみ
   
7. **指示語（明確に何を指すか説明された場合）**
   - 「それ」「あれ」→「何を指すか」が明確な場合のみ

**抽出しないもの:**
- 一般的すぎる言葉（「プロジェクト」「会議」など）
- 説明のない抽象語
- 文脈が不明な指示語

【複数文脈の判定基準】
isAmbiguous: true にするのは以下の場合のみ：
- 同じ用語が**明確に異なる意味**で使われている
- 時系列で定義が変化・明確化されている

【contextの書き方】
- 1-2文で具体的に: 「フロントエンド開発の話をしている時」
- 避けるべき: 「〜について話している時」だけの抽象的な表現

【definitionの書き方】
- 具体的に: 「React Hooksを使った状態管理の方法」
- 避けるべき: 「開発手法の一つ」のような抽象的な説明

【examplesの選び方】
- その用語が**実際にどう使われたか**がわかる発言を2個選ぶ
- 定義を含む発言があれば必ず含める

【frequencyの設定】
その用語が会議で何回くらい言及されたか（概算）：
- 1-2回: 1-2
- 数回: 3-5  
- 頻繁: 5-10
- 非常に多い: 10+

【重要な出力制約】
⚠️ **絶対に守ること**:
1. **JSON形式のみを出力**
2. **説明文、前置き、後書き、コメント、コードブロックは一切不要**
3. **\`\`\`json や \`\`\` などのマークダウン記号は使わない**
4. **最初の文字は必ず { で、最後の文字は必ず } であること**
5. 出力例: {"terms": [...]} のような形式のみ

【その他の制約】
- 10-15個の用語を抽出（質を重視）
- 各用語の各文脈に必ず2個の発言例をつける
- 発言例は会議から**そのまま引用**（改変しない）
- definitionは具体的に（抽象的な説明を避ける）
- 一般的すぎる用語は除外

【文字起こし内容】
${transcript}

⚠️ **繰り返し**: JSONのみを出力してください。説明文やコードブロックは不要です。`;
}

export default {
  getExtractingPrompt
};