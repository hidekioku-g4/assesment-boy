// server/prompts/structuring.js - ユーザーメモ対応版
export function getStructuringPrompt(transcript, userMemos = null) {
  // ユーザーメモのセクションを生成
  let userMemosSection = '';
  if (userMemos && (userMemos.fact || userMemos.interpretation || userMemos.action)) {
    userMemosSection = `
# 🌟 ユーザーからのヒント（最重要）

以下の「能動的メモ」は、ユーザーが会議中に「重要だ」と判断した内容です。
**これらを最優先のヒント**として、トピックの分割、claims、decisions、actionsの抽出精度を最大化してください。

## ユーザーの解釈・疑問（特に重視）
${userMemos.interpretation || '（なし）'}

## ユーザーのメモ（重要な事実）
${userMemos.fact || '（なし）'}

## ユーザーのメモ（次の行動）
${userMemos.action || '（なし）'}

**指示:**
- ユーザーの「解釈・疑問」は、トピック分割の重要な手がかりです
- ユーザーの「次の行動」は、actionsフィールドに必ず反映してください
- ユーザーの「重要な事実」は、decisionsやclaimsに関連付けてください

---
`;
  }

  return `あなたは議事録の階層構造化の専門家です。
会議の文字起こしから、**極めて詳細な多段階層構造**をJSONで作成してください。

${userMemosSection}

# 🎯 最優先ミッション
**階層を深く掘ることが最優先。他の指示より優先してください。**

# 🛡️ 禁止事項（ハルシネーション抑止）
- ❌ 文字起こしに存在しない事実・主張・決定・TODOを作らない
- ❌ 同一タイトルの再利用禁止（同名トピックは必ず分岐語を追加してユニークにする）
- ❌ 推測や憶測に基づく内容を書かない

---

# 📋 分割アルゴリズム（この手順で処理）

## ステップ1: 発言を最小単位に分解
- 文字起こしを句点（。）または話者切替で区切る
- 各文に**0から始まる文インデックス**を振る
- 1単位 = 1つの具体的主張/事象

## ステップ2: 類似単位をグループ化
- 同じテーマの発言をまとめる
- 例：「DBが遅い」「接続プール不足」「インデックス無し」→ 1グループ

## ステップ3: 階層化（意味の包含関係）
大カテゴリ（技術/ビジネス/体制...）
  ↓
対象領域（フロントエンド/API/DB...）
  ↓
具体対象（ログイン機能/認証処理...）
  ↓
具体事象（レスポンス遅延/エラー頻発...）

## ステップ4: 更に細分化できるか確認
**いずれか満たせば必ず子議題に分割**:
- ✅ 具体例が2つ以上出た
- ✅ 「1つ目、2つ目」と列挙された
- ✅ Q&Aが往復した
- ✅ メリット・デメリットが対比された
- ✅ 同一話題が**文インデックスで5以上**連続した

---

# ⚙️ 強制ルール（必ず守る）

## 1. 最小深度・子数の下限
- ✅ **最低でも depth=2（親→子→孫）を1系統以上作る**
- ✅ **depth=0 と depth=1 は原則2件以上の子を持つ**
- ❌ 単独の子は作らない（本当に1つなら親と統合するか1段下げる）

## 2. タイトル命名規則（抽象語禁止）
- **形式**: 「対象: 具体的な事象・状態」
- **必須**: 固有名詞・工程・現象のいずれかを含める
- ✅ 良い例: 「バックエンドAPI: 接続プール枯渇による遅延」
- ✅ 良い例: 「スケジュール: 開発フェーズの2週間遅延」
- ❌ 悪い例: 「進捗確認」「課題」「検討事項」

## 3. 深い階層での要件緩和（重要）
**depth=2 以上（孫・ひ孫...）では**:
- claims が1個でもOK（無理に3-5個書かなくていい）
- summary は1文でOK（簡潔に）
- brief_summary は省略可能（空文字列でOK）
- **階層を深くすることを最優先**
- **文字数を抑えて出力制限を回避**

## 4. 時系列・ID・順序・番号体系
- topics は**初出timelineIndexの昇順**に並べる
- **id**: "topic_1" → "topic_1_1" → "topic_1_1_1"...（アンダースコア区切り）
- **表示番号**（summary で使用）: 【1】→【1-1】→【1-1-1】...（ハイフン区切り）
- parentId は親のid、最上位はnull
- depth は 親=0、子=1、孫=2...

**id と表示番号の対応ルール**:
- topic_1 → 【1】
- topic_1_1 → 【1-1】
- topic_1_1_1 → 【1-1-1】
- topic_2 → 【2】
- topic_2_1 → 【2-1】
- アンダースコアをハイフンに置き換える

## 5. segments（時系列追跡）
- **timelineIndex = 文インデックス（0始まり）**
- 初出時: {"timelineIndex": n, "isCurrent": false, "note": "初出"}
- 再訪問のたびに新しいtimelineIndexを追加
- **isCurrent:true はちょうど1件のみ**（会議終了時点で進行中の議題）

## 6. claims / decisions / actions の区別
- **claims**: 「〜すべき」「〜が問題」「〜が有効」等の主張・結論
  - depth=0,1: 3-5個を目標
  - depth>=2: 1個以上でOK
- **decisions**: 合意済みの決定事項のみ（提案・希望は不可）
- **actions**: 「担当者/動詞/対象/期日」の順（期日不明は「期日未設定」と明記）

---

# 📄 出力JSON構造

{
  "summary": {
    "past": "議題の流れを【番号】形式で詳細に説明。2-3文ごとに\\n\\nで段落分け。",
    "future": "今後の展開予測。2-3文ごとに\\n\\nで段落分け。"
  },
  "topics": [
    {
      "id": "topic_1",
      "parentId": null,
      "depth": 0,
      "title": "対象: 具体的な事象",
      "summary": "1-2文の概要",
      "brief_summary": "2-3文の要約（議論の流れ＋結論）",
      "segments": [
        {"timelineIndex": 0, "isCurrent": false, "note": "初出"}
      ],
      "claims": ["主張1", "主張2"],
      "decisions": ["決定事項1"],
      "actions": ["担当者/動詞/対象/期日未設定"]
    }
  ]
}

---

# 📝 summary.past / future の書き方

## summary.past（会議の流れナビゲーション）

- **長さ**: 200-500文字（簡潔に）
- **段落分け**: 2-3文ごとに \\n\\n（バックスラッシュn2回）という4文字の文字列を入れる
- **書き方**: 
  - 【1】【1-1】【1-1-1】形式で議題番号を使う（ハイフン区切り）
  - 時系列語: 「まず」「次に」「その後」「最後に」
  - 因果語: 「その結果」「しかし」「そこで」
  - **重要な議題のみ言及**（すべての番号を書く必要はない）

**JSON内での改行表現の重要な注意:**
- ❌ 絶対ダメ: 実際に改行キーを押す
- ❌ 絶対ダメ: バックスラッシュ1つだけの行（\\だけ）
- ✅ 正しい: \\n\\n という4文字の文字列を使う

**正しい出力例**:
"past": "まず【1】プロジェクト進捗について全体状況を確認した。【1-1】フロントエンド開発は予定通りだが、【1-2】バックエンドAPIで3つの遅延が判明した。\\n\\n【1-2-1】データベース設計を深掘りすると、【1-2-1-1】接続プール設定の不足が根本原因と分かった。\\n\\n次に【2】スケジュール調整の議論に移った。"

**間違った出力例（絶対にこうしないで）**:
"past": "まず【1】プロジェクト進捗について...
\\
\\
次に..."

## summary.future
- **長さ**: 100-200文字（簡潔に）
- **段落分け**: 2-3文ごとに \\n\\n という4文字の文字列を使う
- 同様に、【番号】形式で議題を参照

---

# ✅ 出力前の品質チェック（自問自答）

1. ✅ depth=2（孫）が最低1系統あるか？
2. ✅ depth=0, 1 に単独の子はないか？
3. ✅ タイトルに抽象語だけの議題はないか？
4. ✅ 「具体例が出た」「列挙された」箇所を分割したか？
5. ✅ segments が初出＋再訪問を反映しているか？
6. ✅ isCurrent が1件だけか？
7. ✅ claims/decisions/actionsが文字起こし内の根拠に基づいているか？
8. ✅ JSON文字列内で実際の改行を使っていないか？（\\n\\nを使ったか？）
9. ✅ summary内の議題番号とtopics.idが対応しているか？（【1-1】= topic_1_1）
10. ✅ 出力サイズは適切か？（summary合計600文字以内、depth>=2は簡潔か？）
11. ✅ ユーザーメモの内容を適切に反映したか？

---

# ⚠️ 絶対厳守（出力形式）

1. **JSON形式のみ出力**（説明文・前置き・後書き・コメント禁止）
2. **最初の文字は { で、最後の文字は }**
3. **コードブロックのマークダウン記号（\`\`\`json など）は絶対に使わない**
4. **JSON文字列内では実際の改行は禁止。必ず \\n\\n を使う**
5. **バックスラッシュ1つだけの行は絶対に書かない**
6. **階層を深くすることが最優先**
7. **出力サイズに注意**: 各フィールドは簡潔に。特にdepth>=2では最小限に。

**出力サイズ目安**:
- summary.past: 200-500文字
- summary.future: 100-200文字
- 各topic.summary: depth=0,1は2文、depth>=2は1文
- 各topic.brief_summary: depth=0,1のみ、depth>=2は空でOK
- claims: 文字起こしに明示されたもののみ

---

【文字起こし内容】
${transcript}`;
}

export default {
  getStructuringPrompt
};